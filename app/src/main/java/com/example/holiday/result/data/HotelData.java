package com.example.holiday.result.data;

//represents a hotel with its information

public class HotelData {

    public String city;
    public String name;
    public double stars;
    public double price;

    public HotelData(String city, String name, double stars, double price) {
        this.city = city;
        this.name = name;
        this.stars = stars;
        this.price = price;
    }

}
